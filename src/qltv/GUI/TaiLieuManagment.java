    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        List<TaiLieu> list = new ArrayList<>();
        try {
            String maTL = txtmatl.getText();
            String tenTL = txttentl.getText();
            String tacGia = txttacgia.getText();
            String theloai = txttheloai.getText();
            String nhaXB = txtnhaXB.getText();
            int namXB = Integer.parseInt(txtnamxb.getText());
            int SL = Integer.parseInt(txtsl.getText());
            TaiLieu tl = new TaiLieu(maTL, tenTL, tacGia, theloai, nhaXB, namXB, SL);
            list = TaiLieuModifiy.timkiem(maTL);
                    if(!list.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Mã tài liệu đã tồn tại, vui lòng nhập lại mã tài liệu !");
                        txtmatl.requestFocus();
                        txtmatl.setText("");
                    }else{
                        TaiLieuModifiy.insert(tl);
                        JOptionPane.showMessageDialog(this, "Bạn đã thêm tài liệu mới thành công.");
                        showTaiLieu();
                        resetData();
                    }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error" +  e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnthemActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        List<TaiLieu> list = new ArrayList<>();    
        try {
            list = TaiLieuModifiy.timkiem(txtmatl.getText());
            if(list.isEmpty()){
                        JOptionPane.showMessageDialog(this, "Mã tài liệu không không tồn tại!");
                        resetData();
                    }else{
                        TaiLieuModifiy.delete(txtmatl.getText());
                        JOptionPane.showMessageDialog(this, "Bạn đã xóa thông tin tài liệu thành công.");
                        showTaiLieu();
                        resetData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error" +  e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnxoaActionPerformed

