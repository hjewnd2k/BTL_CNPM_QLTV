    private void showTaiLieu(){
        List<TaiLieu> TaiLieuList = TaiLieuModifiy.finAll();
        
        tableModel.setRowCount(0);
        TaiLieuList.forEach((tailieu) -> {
            tableModel.addRow(new Object[]{tailieu.getMaTL(),
                tailieu.getTenTL(),tailieu.getTacgia(),tailieu.getTheloai(),tailieu.getNXB(),tailieu.getNamXB(),
                tailieu.getSoluong()});
        });
    }
    
